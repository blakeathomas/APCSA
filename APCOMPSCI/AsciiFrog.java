public class AsciiFrog {
  public static void main(String[] args) {
    System.out.println("     _    _    ");
    System.out.println("    (0)--(0)   ");
    System.out.println("   /-______-\\ ");
    System.out.println("   \\________/  ");
    System.out.println("  ./        \\. ");
    System.out.println(" ( .        . )");
    System.out.println("  \\ \\_\\\\//_/ /");
    System.out.println("   \"\"  \"\"  \"\"");
  }
}